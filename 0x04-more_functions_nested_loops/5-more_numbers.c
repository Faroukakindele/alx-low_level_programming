#include "main.h"

/**
 * print_most_numbers - prints the numbers, from 0 to 9, followed by a new line but not 2 or 4
 * Return: nothing
 */

void more_numbers(void)
{
	int i;

	for (i = 0; i < 10; i++)
	{
		for (i = 0; i <= 14; i++)
		{
			_putchar(i);
		}
		
		_putchar('\n');
	}
	_putchar('\n');

}